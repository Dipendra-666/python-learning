num1 = 3.5
num2 = 2.5

# addition
result1 = num1 + num2
print("addition is", result1)

# subtraction
result2 = num1 - num2
print("subtraction is", result2)

# multiplication
result3 = num1 * num2
print("multiplication is", result3)

# division
result4 = num1 / num2
print("division is", result4)

# round
result5 = round(3.1457374683, 2)
print("rounded number is", result5)
